"""
deckboss-fab — CAD/CAM equipment for deckboss git-agents

Equipment that lets git-agents generate, modify, and validate
CAD drawings and CAM instructions for physical hardware.

Dependencies (user-installed):
  - OpenSCAD:  sudo apt install openscad
  - FreeCAD:   sudo apt install freecad
  - Blender:   sudo apt install blender
  - Gazebo:    sudo apt install gazebo
  - PrusaSlicer/Cura: for G-code from STL

Git-agent usage:
  A git-agent receives a hardware spec (dimensions, mounting holes, ports)
  and generates OpenSCAD source files that produce STLs for 3D printing
  or DXFs for CNC cutting.
"""

